package com.example.kotlin_study

/*
    [ kotlin 기본 data type ]
    - import 없이 바로 사용할수 있는 data type

    Byte, Short, Int, Long
    Float, Double
    Boolean, Char
    String

    주석 색상: 1C9319
 */

fun main(){
    //Byte type
    val num1:Byte = 10
    //Short type
    val num2:Short = 20
    //Int type
    val num3:Int = 30
    //Long type
    val num4:Long = 40
    //Float type
    val num5:Float = 10.1f
    //Double type
    val num6:Double = 10.2
    //String type
    val myName:String = "김구라"
    //Boolean type
    val isRun:Boolean = true
    val isWait:Boolean = false
    //Char type
    val ch1:Char = 'a'  // single 따옴표로 감싸서 Char type 만들기

}